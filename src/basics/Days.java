package basics;

public class Days {

	public static void main(String[] args) {
		String dayofweek = "Monday";
		
		switch (dayofweek) {
		
		case "Monday":
			System.out.println("It's Monday!");
			
			
		case "Thursday":
			System.out.println("It's Thursday!");
			break;
			
		
		default:
			System.out.println("It's a day");
		}

	}

}
